export function buildPlanPaneInstruction(userMessage: string) {
  return [
    'When useful, transform the user request into this compact plan pane format:',
    'goal: one short paragraph + constraints',
    'architecture: systems + flow_summary',
    'stages: stable ordered stages with ids s1, s2, s3...',
    'risks: only action-changing risks',
    'permissions: allow | needs user takeover | deny',
    'definition_of_success: outcomes + evidence',
    '',
    `User request: ${userMessage}`,
  ].join('\n');
}
