export async function buildAiGenericGeneratedAppFiles(job: AppBuilderJob, handoff: AppBuilderRuntimeHandoff): Promise<GeneratedFile[] | null> {
  const blueprint = await buildBlueprint(job);
  if (!blueprint) return null;
  const appSlug = safeSegment(job.id);
  const migrationStamp = timestampForMigration();
  return [
    { path: `app/generated-apps/${appSlug}/page.tsx`, content: frontendContent(job, handoff, blueprint), evidenceKind: 'frontend' },
    { path: `app/api/generated-apps/${appSlug}/${blueprint.apiPathName}/route.ts`, content: apiContent(job, blueprint), evidenceKind: 'backend_api' },
    { path: `supabase/migrations/${migrationStamp}_create_${blueprint.tableName}_${appSlug.slice(0, 8)}.sql`, content: migrationContent(job, blueprint), evidenceKind: 'database_migration' },
    { path: `docs/dsg-generated-apps/${appSlug}.md`, content: runbookContent(job, handoff, blueprint), evidenceKind: 'documentation' },
  ];
}
