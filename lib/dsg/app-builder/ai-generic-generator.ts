function apiContent(job: AppBuilderJob, blueprint: AiAppBlueprint) {
  const appId = jsonString(job.id);
  const tableName = blueprint.tableName;
  return `import { NextResponse } from 'next/server';

type SupabaseRequest = { method?: 'GET' | 'POST'; path: string; query?: string; body?: unknown };
type RecordRow = { id: string; app_id: string; title: string; status: string; evidence: Record<string, unknown>; created_at: string };

const APP_ID = ${appId};
const TABLE_NAME = '${tableName}';

function supabaseConfig() {
  const url = process.env.SUPABASE_URL;
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !key) throw new Error('GENERATED_APP_SUPABASE_ENV_REQUIRED');
  return { url: url.endsWith('/') ? url.slice(0, -1) : url, key };
}

async function supabaseRest<T>(input: SupabaseRequest): Promise<T> {
  const { url, key } = supabaseConfig();
  const response = await fetch(\`\${url}/rest/v1/\${input.path}\${input.query ?? ''}\`, {
    method: input.method ?? 'GET',
    headers: { apikey: key, authorization: \`Bearer \${key}\`, 'content-type': 'application/json', prefer: 'return=representation' },
    body: input.body === undefined ? undefined : JSON.stringify(input.body),
  });
  const text = await response.text();
  const data = text ? JSON.parse(text) : null;
  if (!response.ok) throw new Error(typeof data?.message === 'string' ? data.message : 'GENERATED_APP_SUPABASE_REQUEST_FAILED');
  return data as T;
}

function fail(error: unknown) {
  const message = error instanceof Error ? error.message : 'GENERATED_APP_REQUEST_FAILED';
  return NextResponse.json({ ok: false, error: { code: message, message } }, { status: 400 });
}

export async function GET() {
  try {
    const rows = await supabaseRest<RecordRow[]>({ path: TABLE_NAME, query: \`?app_id=eq.\${encodeURIComponent(APP_ID)}&select=id,app_id,title,status,evidence,created_at&order=created_at.desc\` });
    return NextResponse.json({ ok: true, data: { appId: APP_ID, records: rows } });
  } catch (error) {
    return fail(error);
  }
}

export async function POST(req: Request) {
  try {
    const body = (await req.json()) as { title?: string; evidence?: Record<string, unknown> };
    const title = body.title?.trim();
    if (!title) throw new Error('GENERATED_APP_TITLE_REQUIRED');
    const rows = await supabaseRest<RecordRow[]>({ method: 'POST', path: TABLE_NAME, body: { app_id: APP_ID, title, status: 'GOVERNED_CREATED', evidence: body.evidence || {} } });
    return NextResponse.json({ ok: true, data: { record: rows[0] } });
  } catch (error) {
    return fail(error);
  }
}
`;
}

function migrationContent(job: AppBuilderJob, blueprint: AiAppBlueprint) {
  return `-- Generated by DSG App Builder AI blueprint for job ${job.id}
-- Title: ${blueprint.title.replace(/\n/g, ' ')}

create extension if not exists pgcrypto;

create table if not exists ${blueprint.tableName} (
  id uuid primary key default gen_random_uuid(),
  app_id text not null,
  title text not null,
  status text not null default 'GOVERNED_CREATED',
  evidence jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists ${blueprint.tableName}_app_id_created_at_idx
  on ${blueprint.tableName}(app_id, created_at desc);

alter table ${blueprint.tableName} enable row level security;
`;
}

